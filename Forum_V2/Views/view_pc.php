    <h2>1. Collecte d'Informations</h2><br>

    <h3>1.1 Informations Personnelles</h3>
    <p>Nous collectons des informations personnelles lors de la création d'un compte sur notre site. Cela peut inclure votre nom, votre adresse e-mail, votre pseudonyme, votre date de naissance, et d'autres informations nécessaires à la création d'un profil utilisateur.</p>

    <h3>1.2 Informations Techniques</h3>
    <p>Nous recueillons des informations techniques, telles que votre adresse IP, votre navigateur, votre système d'exploitation, et d'autres données relatives à votre utilisation du site. Ces informations nous aident à améliorer la performance et la sécurité de notre site.</p>

    <h2>2. Utilisation des Informations</h2>
    <p>Nous utilisons vos informations pour fournir, maintenir, protéger, et améliorer notre site. Cela inclut la personnalisation du contenu, la communication avec vous, la résolution de problèmes techniques, et la prévention des activités frauduleuses ou illicites.</p>

    <h2>3. Partage d'Informations</h2>
    <p>Nous ne partageons pas vos informations personnelles avec des tiers sans votre consentement, sauf lorsque cela est nécessaire pour assurer le bon fonctionnement du site, pour répondre aux exigences légales, ou pour protéger nos droits.</p>

    <h2>4. Sécurité des Données</h2>
    <p>Nous prenons des mesures de sécurité appropriées pour protéger vos informations contre l'accès non autorisé, la modification, la divulgation ou la destruction non autorisée. Cela inclut le chiffrement des données sensibles et la sécurisation de nos serveurs.</p>

    <h2>5. Durée de Conservation</h2>
    <p>Nous conservons vos informations aussi longtemps que nécessaire pour fournir nos services et respecter nos obligations légales. Si vous souhaitez supprimer votre compte, veuillez nous contacter à l'adresse e-mail indiquée ci-dessous.</p>

    <h2>6. Cookies</h2>
    <p>Nous utilisons des cookies pour améliorer votre expérience sur notre site. Vous pouvez désactiver les cookies dans les paramètres de votre navigateur, mais cela peut affecter certaines fonctionnalités du site.</p>

